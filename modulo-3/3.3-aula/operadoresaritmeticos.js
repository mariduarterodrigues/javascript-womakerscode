let num1 = 20;
let num2 = 10;

//Adição + 

let totaladicao = num1 + num2;
console.log(totaladicao);
 
// Subtração -

let totalsubtracao = num1 - num2;
console.log(totalsubtracao);

//Multiplicação *

let totalmultiplicação = num1 * num2;
console.log(totalmultiplicação);

//Divisão /

let totaldivisao = num1 / num2;
console.log(totaldivisao);

//Incremento ++ Loops for, do-do while

console.log(num1++);
console.log(++num1);

//Decremento --

console.log(num2--);
console.log(--num2);